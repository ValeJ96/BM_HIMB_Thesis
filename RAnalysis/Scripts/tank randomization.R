#Tank Randomization
rm(list=ls()) # removes all prior objects
day1 <- c(1:8)
day3 <- c(9:16)
day5 <- c(17:24)

ambient = sample(day3,4)
#For day 3 ambient = c(12,15,16,9)
#For day 5 ambient = c(19,17,24,23)
tank1 = sample(ambient,2)
#For day 3 ambient = c(9,12)
#For day 5 ambient = c(19,24)
tank2 = setdiff(ambient,tank1)
#For day 3 ambient = c(15,16)
#For day 5 ambient = c(17,23)
hot = setdiff(day3,ambient)
#For day 3 hot = c(10,11,13,14)
#For day 5 hot = c(18,20,21,22)
tank3 = sample(hot,2)
#For day 3 hot = c(13,14)
#For day 5 hot = c(18,21)
tank4 = setdiff(hot,tank3)
